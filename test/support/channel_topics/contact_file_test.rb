require 'test_helper'

module ChannelTopics
  class ContactFileTest < ActiveSupport::TestCase
    test 'extracts simple information from a vcard' do
      contact_file = ContactFile.new( simple_contact_data )
      expected_results = ["Anik Ganguly", "+1-734-542-5955", "Raleigh/North America", "ganguly@testuser.org"]
      assert_equal expected_results, contact_file.person_data.values_at(:name, :mobile, :timezone, :email)
    end

    test 'extracts complex information from a vcard' do
      contact_file = ContactFile.new( complex_contact_data )
      expected_results = [" TJ", "8282843065", nil, "tj.h.hughes@gmail.com"]
      assert_equal expected_results, contact_file.person_data.values_at(:name, :mobile, :timezone, :email)
    end

    test 'extracts a photo from a vcard' do
      contact_file = ContactFile.new( complex_contact_data )
      assert_equal 41732, contact_file.photo_binary.length
    end

